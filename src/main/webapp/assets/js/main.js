// Menu Scroll Effect
window.addEventListener('scroll', function() {
    const mainMenu = document.querySelector('.main-menu-wrap');
    const scrollIndicator = document.querySelector('.scroll-indicator');
    
    if (window.scrollY > 50) {
        mainMenu.classList.add('scrolled');
    } else {
        mainMenu.classList.remove('scrolled');
    }
    
    // Update scroll indicator
    const winScroll = document.body.scrollTop || document.documentElement.scrollTop;
    const height = document.documentElement.scrollHeight - document.documentElement.clientHeight;
    const scrolled = (winScroll / height) * 100;
    if (scrollIndicator) {
        scrollIndicator.style.width = scrolled + '%';
    }
});

// Mobile Menu Toggle
document.addEventListener('DOMContentLoaded', function() {
    const mobileMenuBtn = document.querySelector('.mobile-menu');
    const mainMenuUl = document.querySelector('nav.main-menu ul');
    
    if (mobileMenuBtn && mainMenuUl) {
        mobileMenuBtn.addEventListener('click', function() {
            mainMenuUl.classList.toggle('show');
        });
    }
    
    // Close menu when clicking outside
    document.addEventListener('click', function(e) {
        if (!e.target.closest('nav.main-menu') && !e.target.closest('.mobile-menu')) {
            if (mainMenuUl.classList.contains('show')) {
                mainMenuUl.classList.remove('show');
            }
        }
    });
    
    // Smooth scroll for anchor links
    document.querySelectorAll('nav.main-menu a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            e.preventDefault();
            const target = document.querySelector(this.getAttribute('href'));
            if (target) {
                target.scrollIntoView({
                    behavior: 'smooth',
                    block: 'start'
                });
                // Close mobile menu after clicking
                mainMenuUl.classList.remove('show');
            }
        });
    });
});

